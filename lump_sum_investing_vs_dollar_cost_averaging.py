def simulate_dca_drip_fractional(ticker_str, w0, t0_str, tf_str=None, freq="M"):
    """Simule une stratégie de Dollar Cost Averaging (DCA) avec réinvestissement

    automatique des dividendes (DRIP) en utilisant des actions fractionnées.

    Parameters:
    -----------
    ticker_str : str
        Le ticker de l'action (ex: 'AAPL', 'MSFT').
    w0 : float
        Le capital total initial à diviser et à déployer.
    t0_str : str
        La date de départ au format 'YYYY-MM-DD'.
    tf_str : str, optional
        La date de fin au format 'YYYY-MM-DD'. Par défaut, t0 + 5 ans.
    freq : str, optional
        La fréquence d'investissement :
        'D' (Journalier), 'W' (Hebdomadaire), 'M' (Mensuel),
        'Q' (Trimestriel), 'S' (Semestriel), 'A' (Annuel).

    Returns:
    --------
    dict
        Métriques de performance et statistiques sur le nombre d'investissements.
    """
    # 1. Gestion et parsing des dates
    t0 = dt.datetime.strptime(t0_str, "%Y-%m-%d")
    if tf_str is None:
        tf = t0 + relativedelta(years=5)
    else:
        tf = dt.datetime.strptime(tf_str, "%Y-%m-%d")

    # Mappage des fréquences pour les adapter au générateur de calendrier Pandas (DatetimeIndex)
    freq_map = {
        "D": "B",  # 'B' = Business day (jours ouvrés, évite les week-ends)
        "W": "W-MON",  # Chaque lundi
        "M": "MS",  # 'MS' = Month Start (premier jour du mois)
        "Q": "BQS",  # 'BQS' = Business Quarter Start
        "S": "6MS",  # Tous les 6 mois (Semestriel)
        "A": "BYS",  # 'BYS' = Business Year Start
    }

    if freq.upper() not in freq_map:
        raise ValueError(
            f"Fréquence indésirable. Choisissez parmi : {list(freq_map.keys())}"
        )

    pandas_freq = freq_map[freq.upper()]

    # Génération du calendrier théorique des dates d'investissement
    # t1 = t0, tN = tf
    investment_dates = pd.date_range(start=t0, end=tf, freq=pandas_freq)

    # N est le nombre total de tranches d'investissement
    N = len(investment_dates)
    if N == 0:
        raise ValueError(
            "Le calendrier d'investissement est vide. Vérifiez l'écart entre t0 et tf."
        )

    # Calcul de la valeur de chaque tranche de déploiement
    w_tranche = w0 / N

    # 2. Téléchargement et alignement des données financières
    ticker = yf.Ticker(ticker_str)
    df = ticker.history(start=t0, end=tf + dt.timedelta(days=5))

    if df.empty:
        raise ValueError(f"Aucune donnée trouvée pour le ticker {ticker_str}.")

    # Suppression des fuseaux horaires
    if df.index.tz is not None:
        df.index = df.index.tz_localize(None)

    # Filtrage strict de la plage de l'historique
    df = df.loc[t0:tf]

    # 3. Ajustement des dates d'investissement aux jours réels de cotation du marché
    # Si une date théorique tombe un jour de fermeture, on prend le premier jour ouvré disponible
    actual_investment_dates = []
    for d in investment_dates:
        # Trouver la première date dans le DataFrame supérieure ou égale à 'd'
        available_dates = df.index[df.index >= d]
        if not available_dates.empty:
            actual_investment_dates.append(available_dates[0])

    # Supprimer les doublons potentiels d'alignement
    actual_investment_dates = sorted(list(set(actual_investment_dates)))

    # On met à jour N et la tranche si l'alignement marché a réduit le nombre de dates réelles
    N = len(actual_investment_dates)
    w_tranche = w0 / N

    print(f"--- CONFIGURATION DU PLAN DCA ---")
    print(f"Nombre total d'investissements prévus (N) : {N}")
    print(f"Montant injecté à chaque occurrence (w)   : {w_tranche:.2f}$\n")

    # 4. Exécution de la simulation chronologique
    shares = 0.0
    total_invested_so_far = 0.0

    print("--- JOURNAL DES OPÉRATIONS (DCA + DRIP) ---")

    # Parcourir chaque jour de bourse chronologiquement
    for date, row in df.iterrows():

        # Cas A : C'est une date d'investissement DCA
        if date in actual_investment_dates:
            p_buy = row["Open"]  # Achat au prix d'ouverture prévu
            new_shares_dca = w_tranche / p_buy
            shares += new_shares_dca
            total_invested_so_far += w_tranche
            print(
                f"[{date.strftime('%Y-%m-%d')}] [DCA] Ingestion de {w_tranche:.2f}$ | "
                f"Achat de {new_shares_dca:.6f} parts à {p_buy:.2f}$ | Total parts : {shares:.6f}"
            )

        # Cas B : Un dividende est payé ce jour-là (DRIP)
        if row["Dividends"] > 0 and shares > 0:
            div_per_share = row["Dividends"]
            p_reinvest = row["Close"]  # Réinvestissement au prix de clôture

            total_div_received = shares * div_per_share
            new_shares_div = total_div_received / p_reinvest
            shares += new_shares_div
            print(
                f"[{date.strftime('%Y-%m-%d')}] [DRIP] Dividende reçu : {total_div_received:.2f}$ | "
                f"Réinvesti en {new_shares_div:.6f} parts à {p_reinvest:.2f}$ | Total parts : {shares:.6f}"
            )

    # 5. Évaluation finale à la clôture de la période
    final_row = df.iloc[-1]
    p_final = final_row["Close"]
    t0_actual = df.index[0]
    tf_actual = df.index[-1]

    w_final = shares * p_final
    total_return = (w_final - w0) / w0

    # Calcul du CAGR basé sur la durée réelle écoulée
    years = (tf_actual - t0_actual).days / 365.25
    cagr = (w_final / w0) ** (1 / years) - 1 if years > 0 else np.nan

    metrics = {
        "Ticker": ticker_str,
        "Fréquence": freq.upper(),
        "Nombre d'investissements (N)": N,
        "Montant par tranche ($)": w_tranche,
        "Date Initiale Réelle": t0_actual.strftime("%Y-%m-%d"),
        "Date Finale Réelle": tf_actual.strftime("%Y-%m-%d"),
        "Capital Initial ($)": w0,
        "Capital Final ($)": w_final,
        "Parts Accumulées Finales": shares,
        "Rendement Total (%)": total_return * 100,
        "CAGR (%)": cagr * 100,
    }

    return metrics

