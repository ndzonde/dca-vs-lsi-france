def compute_hpr(equity_curve: pd.Series, w0: float) -> float:
    """
    Calcule le Holding Period Return (HPR) d'une fenêtre.
    HPR = (W_final - W0) / W0

    Rôle : appelée 2×N fois par run_rolling_window_analysis(), une fois par
    (fenêtre × stratégie). Les séries hpr_lsi[] et hpr_dca[] alimentent ensuite
    run_statistical_tests().
    """
    return (float(equity_curve.iloc[-1]) - w0) / w0


def compute_sharpe_sortino(equity_curve: pd.Series, rf_annual: float = 0.02) -> dict:
    """
    Calcule Sharpe et Sortino annualisés depuis une equity curve journalière.

    Méthode :
        r_t        = ln(P_t / P_{t-1})   rendements log journaliers
        excess_r   = r_t - rf/252
        Sharpe     = mean(excess_r) / std(excess_r) × sqrt(252)
        Sortino    = mean(excess_r) / downside_std   × sqrt(252)
        downside_std = std des excess_r négatifs seulement

    Rôle : appelée 2×N fois par run_rolling_window_analysis().
    Retourne nan si trop peu de données (< 5 observations).
    """
    log_r = np.log(equity_curve / equity_curve.shift(1)).dropna()
    if len(log_r) < 5:
        return {"sharpe": np.nan, "sortino": np.nan}

    rf_d      = rf_annual / 252
    exc       = log_r - rf_d
    sharpe    = exc.mean() / exc.std(ddof=1) * np.sqrt(252) if exc.std(ddof=1) > 0 else np.nan

    neg       = exc[exc < 0]
    down_std  = neg.std(ddof=1) if len(neg) > 1 else np.nan
    sortino   = exc.mean() / down_std * np.sqrt(252) if (down_std and down_std > 0) else np.nan

    return {"sharpe": sharpe, "sortino": sortino}


