import pandas as pd

def run_backtest_for_ticker(ticker_str: str, **kwargs) -> pd.DataFrame:
    """
    Exécute le backtest multi-fréquence LSI vs DCA pour un ticker donné
    et retourne le tableau comparatif mis en forme.
    """

    FREQ_CONFIGS = [
        ("M",  "DCA mensuel  (12×/an)"),
        ("S",  "DCA semestriel (2×/an)"),
        ("A",  "DCA annuel    (1×/an)"),
    ]

    METRICS = [
        ("HPR (%)",     "lsi_hpr",     "dca_hpr",     100),
        ("CAGR (%)",    "lsi_cagr",    "dca_cagr",    100),
        ("Sharpe",      "lsi_sharpe",  "dca_sharpe",    1),
        ("Sortino",     "lsi_sortino", "dca_sortino",   1),
        ("W_final (€)", "lsi_w_final", "dca_w_final",   1),
    ]

    # Configuration de base par défaut
    COMMON = dict(
        ticker_str   = ticker_str,
        w0           = 12_000,
        t0_str       = "2010-01-04",
        window_years = 5,
        shift_months = 1,
        n_windows    = 60,
        rf_annual    = 0.02,
        alpha        = 0.05,
        verbose      = False,
    )

    # Écrase les paramètres par défaut si tu passes des arguments optionnels (ex: w0=24000)
    COMMON.update(kwargs)

    # ── 1. Collecte des résultats ────────────────────────────────────
    runs = {}
    for freq, label in FREQ_CONFIGS:
        if COMMON.get("verbose"):
            print(f"\n>>> {label} pour {ticker_str}")
        runs[freq] = main_comparison(**COMMON, dca_freq=freq)

    # ── 2. Construction du tableau ───────────────────────────────────
    rows = []
    for freq, label in FREQ_CONFIGS:
        df = runs[freq]["results"]
        for metric_label, col_lsi, col_dca, scale in METRICS:
            lsi_v = df[col_lsi].dropna() * scale
            dca_v = df[col_dca].dropna() * scale

            rows.append({
                "Fréquence DCA" : label,
                "Métrique"      : metric_label,
                "LSI mean"      : lsi_v.mean(),
                "LSI std"       : lsi_v.std(),
                "LSI median"    : lsi_v.median(),
                "DCA mean"      : dca_v.mean(),
                "DCA std"       : dca_v.std(),
                "DCA median"    : dca_v.median(),
                "Δ mean (LSI−DCA)" : lsi_v.mean() - dca_v.mean(),
                "LSI wins (%)"  : (df[col_lsi] > df[col_dca]).mean() * 100,
            })

    table = pd.DataFrame(rows).set_index(["Fréquence DCA", "Métrique"])
    return table

