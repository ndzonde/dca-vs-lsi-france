    print("\n" + "─" * 70)
    print("  STATISTIQUES DESCRIPTIVES")
    print("─" * 70)
    print(df_summary.to_string())
    lsi_wins = (df_results["hpr_diff"] > 0).sum()
    print(f"\n  LSI > DCA dans {lsi_wins}/{len(df_results)} fenêtres ({lsi_wins/len(df_results)*100:.1f}%)")

    # --- ÉTAPE 3 : Tests statistiques ---
    print("\n[3/5] Tests statistiques...")
    df_tests = run_statistical_tests(df_results, alpha=alpha)
    print("\n" + "─" * 70)
    print(f"  TESTS STATISTIQUES  (H0 : HPR_LSI = HPR_DCA  |  α={alpha})")
    print("─" * 70)
    print(df_tests.to_string())

    # --- ÉTAPE 4 : Tableaux analytiques ---
    print("\n[4/5] Construction des tableaux analytiques...")
    tb   = table_top_bottom_windows(df_results, n=5)
    reg  = table_regime_breakdown(df_results)
