def simulate_lump_sum_drip_fractional(ticker_str, w0, t0_str, tf_str=None):
    """Simule une stratégie Lump Sum avec réinvestissement automatique des dividendes (DRIP)

    en utilisant des actions fractionnées (l'intégralité de l'argent est investie).
    """
    # 1. Gestion des dates
    t0 = dt.datetime.strptime(t0_str, "%Y-%m-%d")
    if tf_str is None:
        tf = t0 + relativedelta(years=5)
    else:
        tf = dt.datetime.strptime(tf_str, "%Y-%m-%d")

    # Téléchargement de l'historique
    ticker = yf.Ticker(ticker_str)
    df = ticker.history(start=t0, end=tf + dt.timedelta(days=5))

    if df.empty:
        raise ValueError(
            f"Aucune donnée trouvée pour le ticker {ticker_str} sur cette période."
        )

    # Suppression du fuseau horaire pour éviter le bug de comparaison
    if df.index.tz is not None:
        df.index = df.index.tz_localize(None)

    # Filtrer strictement entre t0 et tf
    df = df.loc[t0:tf]

    if df.empty:
        raise ValueError(
            f"Le segment de données entre {t0_str} et {tf.strftime('%Y-%m-%d')} est vide."
        )

    # 2. Investissement Initial à t0 (Tout est investi, pas d'arrondi)
    initial_row = df.iloc[0]
    p_init = initial_row["Open"]  # Achat au prix d'ouverture
    t0_actual = df.index[0]

    shares = w0 / p_init  # Nombre de parts exact (avec virgule)
    cash = 0.0  # Par définition, tout est investi

    print(f"--- Investissement Initial ({t0_actual.strftime('%Y-%m-%d')}) ---")
    print(f"Prix d'ouverture : {p_init:.2f}$")
    print(f"Parts achetées   : {shares:.6f}")
    print(f"Solde cash       : {cash:.2f}$\n")

    # 3. Gestion des dividendes et réinvestissement fractionné
    div_events = df[df["Dividends"] > 0]

    print("--- Suivi des dividendes et réinvestissements (Fractions) ---")
    for date, row in div_events.iterrows():
        div_per_share = row["Dividends"]
        p_reinvest = row["Close"]

        # Tout le cash reçu des dividendes est réinvesti
        total_div_received = shares * div_per_share

        # Nouvelles fractions d'actions achetées
        new_fractions = total_div_received / p_reinvest
        shares += new_fractions  # S'ajoute directement au portefeuille

        print(
            f"[{date.strftime('%Y-%m-%d')}] Div/Part: {div_per_share:.3f}$ | Recu: {total_div_received:.2f}$ | "
            f"+{new_fractions:.6f} parts à {p_reinvest:.2f}$ | Total Parts: {shares:.6f}"
        )

    # 4. Évaluation Finale à tf
    final_row = df.iloc[-1]
    p_final = final_row["Close"]  # Valeur à la dernière clôture
    tf_actual = df.index[-1]

    w_final = shares * p_final

    # 5. Calcul des Métriques
    total_return = (w_final - w0) / w0
    years = (tf_actual - t0_actual).days / 365.25
    cagr = (w_final / w0) ** (1 / years) - 1 if years > 0 else np.nan

    metrics = {
        "Ticker": ticker_str,
        "Date Initiale": t0_actual.strftime("%Y-%m-%d"),
        "Date Finale": tf_actual.strftime("%Y-%m-%d"),
        "Capital Initial ($)": w0,
        "Capital Final ($)": w_final,
        "Parts Finales": shares,
        "Cash Final ($)": cash,
        "Rendement Total (%)": total_return * 100,
        "CAGR (%)": cagr * 100,
    }

    return metrics

