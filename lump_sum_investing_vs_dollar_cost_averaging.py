def fetch_price_history(ticker_str: str, t0: dt.datetime, tf: dt.datetime) -> pd.DataFrame:
    """
    Télécharge et nettoie l'historique OHLCV + Dividendes d'un ticker sur [t0, tf].

    Rôle dans le flux global :
        Appelée par simulate_lsi et simulate_dca pour éviter la duplication.
        Chaque fenêtre glissante déclenche exactement deux appels (un par stratégie).

    Important — auto_adjust=False :
        yfinance renvoie par défaut (auto_adjust=True) des prix DÉJÀ ajustés
        des dividendes ET des splits. Le moteur de simulation réinvestit
        pourtant explicitement les dividendes via DRIP (colonne "Dividends",
        toujours brute quel que soit auto_adjust) — avec le défaut, cela
        comptait CHAQUE dividende deux fois (une fois dans le prix ajusté,
        une fois dans le DRIP manuel), gonflant artificiellement tous les
        HPR/CAGR/Sharpe. On force donc des prix bruts, et les splits sont
        gérés explicitement dans simulate_* via la colonne "Stock Splits".

    Returns
    -------
    pd.DataFrame — Index DatetimeIndex timezone-naive, colonnes OHLCV +
    Dividends + Stock Splits (prix bruts, non ajustés).
    """
    ticker = yf.Ticker(ticker_str)
    df = ticker.history(start=t0, end=tf + dt.timedelta(days=5), auto_adjust=False)

    if df.empty:
        raise ValueError(f"Aucune donnée pour {ticker_str} entre {t0.date()} et {tf.date()}.")

    if df.index.tz is not None:
        df.index = df.index.tz_localize(None)

    df = df.loc[t0:tf]

    if df.empty:
        raise ValueError(f"Segment vide pour {ticker_str} entre {t0.date()} et {tf.date()}.")

    return df


