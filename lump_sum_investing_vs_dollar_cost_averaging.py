    print("\n  Bottom 5 fenêtres (DCA domine le plus) :")
    print(tb["bottom"].to_string(index=False))
    print("\n  Breakdown par régime de marché :")
    print(reg.to_string())

    # --- ÉTAPE 5 : Construction des figures (lazy) ---
    print("\n[5/5] Préparation des graphes (non affichés)...")
    matplotlib.use("Agg")   # backend non-interactif pendant la construction

    plots = {
        "violin_hpr"            : plot_violin_hpr(df_results, ticker_str),
        "scatter_temporal"      : plot_scatter_temporal(df_results, ticker_str),
        "hpr_diff_vs_market"    : plot_hpr_diff_vs_market(df_results, ticker_str),
        "sharpe_temporal"       : plot_sharpe_temporal(df_results, ticker_str),
        "win_rate_bar"          : plot_win_rate_bar(df_results, ticker_str),
        "hpr_diff_distribution" : plot_hpr_diff_distribution(df_results, ticker_str),
    }
    plt.close("all")   # libère la mémoire, les objets Figure restent accessibles

    print("  ✓ Graphes disponibles :")
    for k in plots:
        print(f"      output['plots']['{k}'].show()")

    print("\n" + "=" * 70)
    print("  DONE")
    print("=" * 70)

    return {
        "results" : df_results,
        "summary" : df_summary,
        "tests"   : df_tests,
        "tables"  : {"top_bottom": tb, "regime_breakdown": reg},
        "plots"   : plots,
    }

