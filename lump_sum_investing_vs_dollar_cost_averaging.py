    print(f"  Rémun. cash DCA : {cash_rf_label}")
    print("=" * 70)

    # --- ÉTAPE 1 : Simulations rolling ---
    print("\n[1/5] Simulation des fenêtres glissantes...")
    df_results = run_rolling_window_analysis(
        ticker_str=ticker_str, w0=w0, t0_str=t0_str,
        window_years=window_years, shift_months=shift_months,
        n_windows=n_windows, dca_freq=dca_freq, rf_annual=rf_annual,
        dca_cash_rf=dca_cash_rf, verbose=verbose,
    )
    print(f"  ✓ {len(df_results)} fenêtres simulées.")

    # --- ÉTAPE 2 : Statistiques descriptives ---
    print("\n[2/5] Statistiques synthétiques...")
    df_summary = compute_summary_statistics(df_results)
