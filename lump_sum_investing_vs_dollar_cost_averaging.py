def simulate_lump_sum_drip_fractional(
    ticker_str: str,
    w0: float,
    t0_str: str,
    tf_str: str | None = None,
    verbose: bool = False,
) -> dict:
    """
    Simule la stratégie Lump Sum Investing (LSI) avec DRIP fractionné.

    Logique :
        - Tout le capital w0 est investi en une seule fois au prix d'ouverture de t0.
        - Chaque dividende est immédiatement réinvesti au prix de clôture (DRIP).
        - Actions fractionnées : pas d'arrondi, cash résiduel = 0.
        - Les splits ne nécessitent aucun traitement manuel : yfinance renvoie
          des prix TOUJOURS ajustés des splits (même avec auto_adjust=False,
          qui ne désactive que l'ajustement dividende) — un traitement
          manuel supplémentaire du ratio "Stock Splits" double-compterait
          l'effet, exactement comme le faisait le bug des dividendes.

    Important :
        La courbe d'equity est reconstruite jour par jour (et pas en multipliant
        le nombre de parts FINAL par toute la série de Close) : sinon, Sharpe et
        Sortino — qui utilisent les rendements journaliers de equity_curve —
        seraient calculés sur une série artificiellement constante en "nombre de
        parts", identique en pratique aux rendements du prix brut, et ignorant
        la vraie trajectoire de richesse (paliers aux dates de DRIP/split).

    Rôle dans le flux global :
        Appelée N fois par run_rolling_window_analysis(). Retourne equity_curve
        (pd.Series journalière) qui alimente compute_hpr() et compute_sharpe_sortino().

    Returns
    -------
    dict : {'metrics': dict, 'equity_curve': pd.Series}
    """
    t0 = dt.datetime.strptime(t0_str, "%Y-%m-%d")
    tf = (t0 + relativedelta(years=5)) if tf_str is None else dt.datetime.strptime(tf_str, "%Y-%m-%d")

    df = fetch_price_history(ticker_str, t0, tf)

    p_init    = df.iloc[0]["Open"]
    t0_actual = df.index[0]
    shares    = w0 / p_init

    if verbose:
        print(f"[LSI] {t0_actual.date()} — {shares:.6f} parts @ {p_init:.2f}$")

    equity_val = []
    for date, row in df.iterrows():
        if row["Dividends"] > 0 and shares > 0:
            div_cash   = shares * row["Dividends"]
            new_shares = div_cash / row["Close"]
            shares    += new_shares
            if verbose:
                print(f"[LSI][DRIP] {date.date()} — +{new_shares:.6f} parts")

        equity_val.append(shares * row["Close"])

    equity_curve = pd.Series(equity_val, index=df.index)

    p_final   = df.iloc[-1]["Close"]
    tf_actual = df.index[-1]
    w_final   = shares * p_final
    years     = (tf_actual - t0_actual).days / 365.25
    total_ret = (w_final - w0) / w0
    cagr      = (w_final / w0) ** (1 / years) - 1 if years > 0 else np.nan

    metrics = {
        "strategy": "LSI", "ticker": ticker_str,
        "t0": t0_actual.strftime("%Y-%m-%d"), "tf": tf_actual.strftime("%Y-%m-%d"),
        "w0": w0, "w_final": w_final, "shares_final": shares,
        "total_return": total_ret, "cagr": cagr, "years": years,
    }
    return {"metrics": metrics, "equity_curve": equity_curve}


def simulate_dca_drip_fractional(
    ticker_str: str,
    w0: float,
    t0_str: str,
    tf_str: str | None = None,
    freq: str = "M",
    cash_rf_annual: float | None = None,
    verbose: bool = False,
) -> dict:
    """
    Simule la stratégie Dollar-Cost Averaging (DCA) avec DRIP fractionné.

    Logique :
        - w0 est divisé en N tranches égales investies à la fréquence `freq`.
        - Chaque tranche est achetée au prix d'ouverture du jour de bourse aligné.
        - Les dividendes intermédiaires sont réinvestis au prix de clôture (DRIP).
        - Le capital pas encore investi ("cash" en attente) est placé au taux
          sans risque et capitalisé quotidiennement (voir plus bas) au lieu de
          dormir à 0%.

    Rémunération du cash en attente :
        À chaque séance, avant tranche/DRIP du jour, le cash restant est
        capitalisé sur le nombre de jours calendaires (Δj) écoulés depuis la
        séance précédente (donc week-ends/jours fériés inclus, pas seulement
        les jours de bourse) :
            cash_t = cash_{t-1} × (1 + r_annual)^(Δj / 365)
        Cette formule est mathématiquement équivalente à une capitalisation
        jour par jour au taux journalier r_annual^(1/365) - 1 répétée Δj fois
        (composition géométrique), donc "chaque jour où l'argent n'est pas
        encore investi" est bien rémunéré, y compris les jours sans séance.
        r_annual = cash_rf_annual si fourni (taux constant, utile pour les
        tests de sensibilité, ex. 0.0 pour retrouver l'hypothèse initiale du
        papier), sinon le taux du Livret A en vigueur à la date
        (get_livret_a_rate) — taux sans risque le plus cohérent pour du cash
        de particulier français en attente d'investissement.

    Rôle dans le flux global :
        Miroir de simulate_lsi(). Retourne la même structure {metrics, equity_curve}
        pour un traitement symétrique dans run_rolling_window_analysis().

    Returns
    -------
    dict : {'metrics': dict, 'equity_curve': pd.Series}
    """
    FREQ_MAP = {"D": "B", "W": "W-MON", "M": "MS", "Q": "BQS", "S": "6MS", "A": "BYS"}
    if freq.upper() not in FREQ_MAP:
        raise ValueError(f"Fréquence invalide. Choisir parmi : {list(FREQ_MAP.keys())}")

    t0 = dt.datetime.strptime(t0_str, "%Y-%m-%d")
    tf = (t0 + relativedelta(years=5)) if tf_str is None else dt.datetime.strptime(tf_str, "%Y-%m-%d")

    df = fetch_price_history(ticker_str, t0, tf)

    # Alignement des dates théoriques sur jours de bourse réels
    inv_dates_theory = pd.date_range(start=t0, end=tf, freq=FREQ_MAP[freq.upper()])
    actual_inv_dates = set()
    for d in inv_dates_theory:
        available = df.index[df.index >= d]
        if not available.empty:
            actual_inv_dates.add(available[0])
    actual_inv_dates = sorted(actual_inv_dates)

    N = len(actual_inv_dates)
    if N == 0:
        raise ValueError("Calendrier DCA vide : vérifier t0 / tf / fréquence.")
    w_tranche = w0 / N

    if verbose:
        print(f"[DCA] N={N} tranches de {w_tranche:.2f}$ | fréquence={freq.upper()}")

    shares     = 0.0
    cash       = w0    # capital pas encore investi, rémunéré au taux sans risque
    prev_date  = df.index[0]
    equity_val = []

    for date, row in df.iterrows():
        # 1. Capitalisation du cash en attente depuis la séance précédente
        n_days = (date - prev_date).days
        if n_days > 0 and cash > 0:
            r_annual = cash_rf_annual if cash_rf_annual is not None else get_livret_a_rate(date)
            cash *= (1 + r_annual) ** (n_days / 365)
        prev_date = date

        # 2. Tranche du jour, le cas échéant
        if date in actual_inv_dates:
            shares += w_tranche / row["Open"]
            cash   -= w_tranche
            if verbose:
                print(f"[DCA] {date.date()} — +{w_tranche/row['Open']:.6f} parts @ {row['Open']:.2f}$ | cash restant={cash:.2f}$")

        # 3. DRIP
        if row["Dividends"] > 0 and shares > 0:
            div_cash   = shares * row["Dividends"]
            shares    += div_cash / row["Close"]

        equity_val.append(shares * row["Close"] + max(cash, 0.0))

    equity_curve = pd.Series(equity_val, index=df.index)

    t0_actual = df.index[0]
    tf_actual = df.index[-1]
    w_final   = float(equity_curve.iloc[-1])
    years     = (tf_actual - t0_actual).days / 365.25
    total_ret = (w_final - w0) / w0
    cagr      = (w_final / w0) ** (1 / years) - 1 if years > 0 else np.nan

    metrics = {
        "strategy": "DCA", "ticker": ticker_str, "freq": freq.upper(),
        "n_investments": N, "w_tranche": w_tranche,
        "t0": t0_actual.strftime("%Y-%m-%d"), "tf": tf_actual.strftime("%Y-%m-%d"),
        "w0": w0, "w_final": w_final, "shares_final": shares, "cash_final": float(cash),
        "total_return": total_ret, "cagr": cagr, "years": years,
    }
    return {"metrics": metrics, "equity_curve": equity_curve}


