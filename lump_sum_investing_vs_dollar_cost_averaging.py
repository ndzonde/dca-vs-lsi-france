def table_top_bottom_windows(df: pd.DataFrame, n: int = 5) -> dict:
    """
    Retourne les N meilleures et N pires fenêtres pour LSI et DCA selon le HPR_diff.

    Lecture :
        Le top-N montre dans quelles conditions de marché LSI surperforme le plus
        (souvent : entrée en bas de cycle, marché baissier précédant une reprise).
        Le bottom-N montre quand DCA protège le plus (entrée haute, marché volatile).

    Rôle dans le flux global :
        Appelée par main_comparison(). Résultat stocké dans output['tables']['top_bottom'].
        Peut être converti en LaTeX via df.to_latex() directement.

    Returns
    -------
    dict : {'top': pd.DataFrame, 'bottom': pd.DataFrame}
    """
    cols = ["window", "t0", "tf", "lsi_hpr", "dca_hpr", "hpr_diff",
            "lsi_sharpe", "dca_sharpe", "lsi_cagr", "dca_cagr"]
    sub  = df[cols].copy()
    sub["lsi_hpr"]   *= 100
    sub["dca_hpr"]   *= 100
    sub["hpr_diff"]  *= 100
    sub["lsi_cagr"]  *= 100
    sub["dca_cagr"]  *= 100

    top    = sub.nlargest(n,  "hpr_diff").reset_index(drop=True)
    bottom = sub.nsmallest(n, "hpr_diff").reset_index(drop=True)

    return {"top": top, "bottom": bottom}


def table_regime_breakdown(df: pd.DataFrame) -> pd.DataFrame:
    """
    Décompose les résultats selon le régime de marché à t0.

    Méthode :
        Le niveau de marché à t0 (market_level_t0) est discrétisé en tertiles :
            - Tertile bas  (T1) : entrée en marché déprimé
            - Tertile moyen(T2) : entrée en marché neutre
            - Tertile haut (T3) : entrée en marché élevé
        Pour chaque tertile, on calcule :
            - HPR moyen LSI vs DCA
            - % fenêtres où LSI > DCA
            - Différentiel médian

    Lecture :
        Si T1 → HPR_diff > 0 et T3 → HPR_diff < 0, on confirme empiriquement
        que LSI est sensible au timing et DCA est la meilleure stratégie pour
        un investisseur ne pouvant pas identifier les bas de cycle.

    Returns
    -------
    pd.DataFrame — stocké dans output['tables']['regime_breakdown'].
    """
    df = df.copy()
    df["regime"] = pd.qcut(
        df["market_level_t0"], q=3,
        labels=["T1 — Marché bas", "T2 — Neutre", "T3 — Marché haut"]
    )

    rows = []
    for regime, grp in df.groupby("regime", observed=True):
        n = len(grp)
        rows.append({
            "Régime"               : regime,
            "N fenêtres"           : n,
            "HPR_LSI moyen (%)"    : grp["lsi_hpr"].mean() * 100,
            "HPR_DCA moyen (%)"    : grp["dca_hpr"].mean() * 100,
            "Diff médiane (%)"     : grp["hpr_diff"].median() * 100,
            "LSI gagne (%)"        : (grp["hpr_diff"] > 0).mean() * 100,
            "Sharpe LSI moyen"     : grp["lsi_sharpe"].mean(),
            "Sharpe DCA moyen"     : grp["dca_sharpe"].mean(),
        })
    return pd.DataFrame(rows).set_index("Régime")


