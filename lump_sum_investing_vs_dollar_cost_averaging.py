# Sous DCA, le capital pas encore investi n'est plus supposé dormir à 0% (biais
# reconnu en faveur du LSI dans la version initiale du papier). On le rémunère
# au taux sans risque le plus cohérent pour un particulier français plaçant du
# cash court terme, liquide et garanti : le Livret A (plutôt qu'un taux
# interbancaire type EONIA/€STR, inaccessible à un particulier). Barème
# officiel (arrêtés successifs, source Banque de France / Selectra / Magnolia) :
LIVRET_A_SCHEDULE = [
    ("2009-08-01", 0.0125),
    ("2010-08-01", 0.0175),
    ("2011-02-01", 0.0200),
    ("2011-08-01", 0.0225),
    ("2013-02-01", 0.0175),
    ("2013-08-01", 0.0125),
    ("2014-08-01", 0.0100),
    ("2015-08-01", 0.0075),
    ("2020-02-01", 0.0050),
    ("2022-02-01", 0.0100),
    ("2022-08-01", 0.0200),
    ("2023-02-01", 0.0300),
]
_LIVRET_A_DATES = pd.to_datetime([d for d, _ in LIVRET_A_SCHEDULE])
_LIVRET_A_RATES = np.array([r for _, r in LIVRET_A_SCHEDULE])


def get_livret_a_rate(date) -> float:
    """
    Taux annuel du Livret A en vigueur à `date`.

    En dehors du barème connu, on prolonge à plat la borne la plus proche
    (avant 2009-08-01 : 1.25% ; après 2023-02-01 : 3.00%) plutôt que
    d'échouer — les fenêtres de l'étude (2010-2020) sont toutes couvertes.
    """
    date = pd.Timestamp(date)
    idx = _LIVRET_A_DATES.searchsorted(date, side="right") - 1
    idx = max(0, min(idx, len(_LIVRET_A_RATES) - 1))
    return float(_LIVRET_A_RATES[idx])


