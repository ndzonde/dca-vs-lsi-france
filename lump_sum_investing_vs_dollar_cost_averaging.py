def run_rolling_window_analysis(
    ticker_str   : str,
    w0           : float,
    t0_str       : str,
    window_years : int,
    shift_months : int,
    n_windows    : int,
    dca_freq     : str  = "M",
    rf_annual    : float = 0.02,
    dca_cash_rf  : float | None = None,
    verbose      : bool  = False,
) -> pd.DataFrame:
    """
    Orchestre l'analyse comparative LSI vs DCA sur N fenêtres glissantes.

    Principe :
        Fenêtre i → [t0 + i×shift_months,  t0 + i×shift_months + window_years]
        Chaque fenêtre est indépendante : nouvelle simulation LSI + DCA + métriques.
        Le paramètre shift_months contrôle la granularité du balayage temporel.

    dca_cash_rf : taux (constant) de rémunération du cash DCA en attente. Si
        None (défaut), utilise le barème historique du Livret A par date
        (get_livret_a_rate) — voir simulate_dca_drip_fractional(). Séparé de
        rf_annual, qui reste le taux utilisé comme référence pour Sharpe/Sortino.

    Rôle dans le flux global :
        Cœur de la COUCHE 2b. Appelle les COUCHES 1 et 2 et retourne un DataFrame
        structuré que compute_summary_statistics() et run_statistical_tests()
        consomment directement, et que les fonctions de visualisation (COUCHE 3)
        exploitent pour les graphes.

    Returns
    -------
    pd.DataFrame — colonnes :
        window, t0, tf, market_level_t0,
        lsi_hpr, lsi_cagr, lsi_sharpe, lsi_sortino, lsi_w_final,
        dca_hpr, dca_cagr, dca_sharpe, dca_sortino, dca_w_final,
        hpr_diff  (LSI − DCA)
    """
    t0_base = dt.datetime.strptime(t0_str, "%Y-%m-%d")
    records = []

    for i in range(n_windows):
        t0_i   = t0_base + relativedelta(months=i * shift_months)
        tf_i   = t0_i   + relativedelta(years=window_years)
        t0_i_s = t0_i.strftime("%Y-%m-%d")
        tf_i_s = tf_i.strftime("%Y-%m-%d")

        if verbose:
            print(f"\n[Fenêtre {i+1:02d}/{n_windows}] {t0_i_s} → {tf_i_s}")

        try:
            lsi = simulate_lump_sum_drip_fractional(ticker_str, w0, t0_i_s, tf_i_s, verbose=False)
            dca = simulate_dca_drip_fractional(ticker_str, w0, t0_i_s, tf_i_s, freq=dca_freq, cash_rf_annual=dca_cash_rf, verbose=False)

            lsi_hpr = compute_hpr(lsi["equity_curve"], w0)
            dca_hpr = compute_hpr(dca["equity_curve"], w0)
            lsi_r   = compute_sharpe_sortino(lsi["equity_curve"], rf_annual)
            dca_r   = compute_sharpe_sortino(dca["equity_curve"], rf_annual)

            # Niveau de marché au point d'entrée (prix de clôture normalisé sur la série entière)
            # Utilisé par plot_hpr_diff_vs_market() pour contextualiser les résultats
            market_level = lsi["equity_curve"].iloc[0] / w0  # = 1.0 au départ, sert de référence relative

            records.append({
                "window"          : i + 1,
                "t0"              : t0_i_s,
                "tf"              : tf_i_s,
                "market_level_t0" : lsi["equity_curve"].iloc[0],   # valeur initiale = w0 × prix_relatif
                "lsi_hpr"         : lsi_hpr,
                "lsi_cagr"        : lsi["metrics"]["cagr"],
                "lsi_sharpe"      : lsi_r["sharpe"],
                "lsi_sortino"     : lsi_r["sortino"],
                "lsi_w_final"     : lsi["metrics"]["w_final"],
                "dca_hpr"         : dca_hpr,
                "dca_cagr"        : dca["metrics"]["cagr"],
                "dca_sharpe"      : dca_r["sharpe"],
                "dca_sortino"     : dca_r["sortino"],
                "dca_w_final"     : dca["metrics"]["w_final"],
                "hpr_diff"        : lsi_hpr - dca_hpr,
            })

            if verbose:
                print(f"  LSI: HPR={lsi_hpr*100:.2f}% | Sharpe={lsi_r['sharpe']:.3f} | Sortino={lsi_r['sortino']:.3f}")
                print(f"  DCA: HPR={dca_hpr*100:.2f}% | Sharpe={dca_r['sharpe']:.3f} | Sortino={dca_r['sortino']:.3f}")

        except Exception as e:
            if verbose:
                print(f"  ⚠ Fenêtre {i+1} ignorée : {e}")
            continue

    if not records:
        raise RuntimeError("Aucune fenêtre simulée. Vérifiez les paramètres.")

    df = pd.DataFrame(records)
    df["t0"] = pd.to_datetime(df["t0"])   # converti pour les graphes temporels
    return df


def compute_summary_statistics(df: pd.DataFrame) -> pd.DataFrame:
    """
    Calcule les statistiques descriptives (mean, std, min, median, max) pour
    chaque métrique et chaque stratégie, sur l'ensemble des fenêtres.

    Rôle dans le flux global :
        Première couche de restitution après run_rolling_window_analysis().
        Produit le tableau «macro» affiché par main_comparison() et disponible
        dans output['summary'].

    Returns
    -------
    pd.DataFrame — Index = métriques, colonnes = LSI_*/DCA_*/Diff_mean.
    """
    pairs = [
        ("HPR (%)",     "lsi_hpr",     "dca_hpr",     100),
        ("CAGR (%)",    "lsi_cagr",    "dca_cagr",    100),
        ("Sharpe",      "lsi_sharpe",  "dca_sharpe",  1  ),
        ("Sortino",     "lsi_sortino", "dca_sortino", 1  ),
        ("W_final ($)", "lsi_w_final", "dca_w_final", 1  ),
    ]
    rows = []
    for label, cl, cd, scale in pairs:
        lv = df[cl].dropna() * scale
        dv = df[cd].dropna() * scale
        rows.append({
            "Métrique"  : label,
            "LSI_mean"  : lv.mean(), "LSI_std": lv.std(), "LSI_min": lv.min(),
            "LSI_median": lv.median(), "LSI_max": lv.max(),
            "DCA_mean"  : dv.mean(), "DCA_std": dv.std(), "DCA_min": dv.min(),
            "DCA_median": dv.median(), "DCA_max": dv.max(),
            "Diff_mean" : lv.mean() - dv.mean(),
        })
    return pd.DataFrame(rows).set_index("Métrique")


def run_statistical_tests(df: pd.DataFrame, alpha: float = 0.05) -> pd.DataFrame:
    """
    Teste si LSI et DCA diffèrent statistiquement sur le HPR.

    Tests appliqués (non paramétriques — robustes aux petits échantillons) :
        1. Mann-Whitney U  : compare les rangs des deux distributions (bilatéral
           + unilatéral pour détecter la direction dominante).
        2. Kruskal-Wallis  : généralisation k-groupes (k=2 ici), extensible
           si on veut ajouter d'autres fréquences DCA.
        3. Wilcoxon Signed-Rank pairé : exploite le design pairé — même fenêtre
           = mêmes conditions de marché, donc variance commune annulée.
           C'est le test le plus puissant des trois pour ce design.

    Rôle dans le flux global :
        Dernière couche analytique de main_comparison(). Le résultat est disponible
        dans output['tests'] et peut être incorporé directement dans un article LaTeX.

    Returns
    -------
    pd.DataFrame — colonnes : Statistique, p_value, Significatif, Interprétation.
    """
    lsi = df["lsi_hpr"].dropna().values
    dca = df["dca_hpr"].dropna().values
    common = df[["lsi_hpr","dca_hpr"]].dropna()
    diff   = (common["lsi_hpr"] - common["dca_hpr"]).values

    res = []

    # Mann-Whitney U
    mw_s, mw_p = stats.mannwhitneyu(lsi, dca, alternative="two-sided")
    _, mw_pg   = stats.mannwhitneyu(lsi, dca, alternative="greater")
    sig        = mw_p < alpha
    res.append({
        "Test": "Mann-Whitney U", "Statistique": round(mw_s, 4), "p_value": round(mw_p, 4),
        "Significatif": "✓" if sig else "✗",
        "Interprétation": ("LSI > DCA" if mw_pg < alpha else "DCA > LSI") if sig else "Pas de différence significative",
    })

    # Kruskal-Wallis
    kw_s, kw_p = stats.kruskal(lsi, dca)
    sig = kw_p < alpha
    res.append({
        "Test": "Kruskal-Wallis", "Statistique": round(kw_s, 4), "p_value": round(kw_p, 4),
        "Significatif": "✓" if sig else "✗",
        "Interprétation": "Distributions différentes" if sig else "Pas de différence significative",
    })

    # Wilcoxon Signed-Rank pairé
    if len(diff) >= 10 and not np.all(diff == 0):
        try:
            wx_s, wx_p = stats.wilcoxon(diff, alternative="two-sided")
            sig = wx_p < alpha
            med = np.median(diff)
            res.append({
                "Test": "Wilcoxon Signed-Rank (pairé)", "Statistique": round(wx_s, 4), "p_value": round(wx_p, 4),
                "Significatif": "✓" if sig else "✗",
                "Interprétation": (f"LSI > DCA (Δméd={med*100:+.2f}%)" if med > 0 else f"DCA > LSI (Δméd={med*100:+.2f}%)") if sig else "Différence non significative",
            })
        except Exception:
            pass

    return pd.DataFrame(res).set_index("Test")


